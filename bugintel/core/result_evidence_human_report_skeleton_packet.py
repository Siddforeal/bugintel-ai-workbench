"""
Human report skeleton packet builder.

This module turns a v0.68 finding draft packet review gate into a safe human
report skeleton packet. It prepares section placeholders only: Summary, Impact,
Steps to Reproduce, Evidence, Affected Assets, Severity Rationale, Remediation,
Blocked Claims / Do Not Claim, and a final human writing checklist.

It does not generate final reports, submit reports, write case memory, write
research state, call providers, execute tools, launch browsers, send network
requests, mutate targets, or confirm vulnerabilities.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any


@dataclass(frozen=True)
class HumanReportSkeletonSection:
    name: str
    status: str
    purpose: str
    placeholder: str
    guidance: tuple[str, ...]
    source_items: tuple[str, ...]

    def to_dict(self) -> dict[str, Any]:
        return {
            "name": self.name,
            "status": self.status,
            "purpose": self.purpose,
            "placeholder": self.placeholder,
            "guidance": list(self.guidance),
            "source_items": list(self.source_items),
        }


@dataclass(frozen=True)
class HumanReportSkeletonPacket:
    recommendation: str
    summary: HumanReportSkeletonSection
    impact: HumanReportSkeletonSection
    steps_to_reproduce: HumanReportSkeletonSection
    evidence: HumanReportSkeletonSection
    affected_assets: HumanReportSkeletonSection
    severity_rationale: HumanReportSkeletonSection
    remediation: HumanReportSkeletonSection
    blocked_claims_do_not_claim: HumanReportSkeletonSection
    human_final_writing_checklist: tuple[str, ...]
    source: str = "result-evidence-human-report-skeleton-packet"
    planning_only: bool = True
    skeleton_state: str = "packet_built_not_report_generated"

    def to_dict(self) -> dict[str, Any]:
        return {
            "kind": "result_evidence_human_report_skeleton_packet",
            "source": self.source,
            "recommendation": self.recommendation,
            "summary": self.summary.to_dict(),
            "impact": self.impact.to_dict(),
            "steps_to_reproduce": self.steps_to_reproduce.to_dict(),
            "evidence": self.evidence.to_dict(),
            "affected_assets": self.affected_assets.to_dict(),
            "severity_rationale": self.severity_rationale.to_dict(),
            "remediation": self.remediation.to_dict(),
            "blocked_claims_do_not_claim": self.blocked_claims_do_not_claim.to_dict(),
            "human_final_writing_checklist": list(self.human_final_writing_checklist),
            "planning_only": self.planning_only,
            "skeleton_state": self.skeleton_state,
            "safety": {
                "local_only": True,
                "planning_only": True,
                "human_approval_required": True,
                "report_generation": False,
                "report_submission": False,
                "final_report": False,
                "state_mutation": False,
                "case_memory_write": False,
                "research_state_write": False,
                "network_interaction": False,
                "target_mutation": False,
                "tool_execution": False,
                "browser_execution": False,
                "llm_provider_calls": False,
                "provider_execution": False,
                "vulnerability_confirmation": False,
            },
        }

    def to_markdown(self, title: str = "Human Report Skeleton Packet") -> str:
        lines: list[str] = [
            f"# {title}",
            "",
            "## Status",
            "",
            f"- Recommendation: {self.recommendation}",
            f"- Skeleton state: {self.skeleton_state}",
            "- Final report generated by Blackhole: false",
            "- Report submission by Blackhole: false",
            "- Human approval required: true",
            "- Vulnerability confirmation: false",
            "- Planning-only: true",
            "",
        ]

        for section in (
            self.summary,
            self.impact,
            self.steps_to_reproduce,
            self.evidence,
            self.affected_assets,
            self.severity_rationale,
            self.remediation,
            self.blocked_claims_do_not_claim,
        ):
            lines.extend(_render_section(section))

        lines.extend(["", "## Human Final-Writing Checklist", ""])
        for item in self.human_final_writing_checklist:
            lines.append(f"- [ ] {item}")

        lines.extend(
            [
                "",
                "## Safety",
                "",
                "- This command builds a report skeleton packet only.",
                "- Do not treat this packet as a final report.",
                "- Do not submit anything from this packet without human writing and evidence review.",
                "- Do not claim impact, severity, affected assets, or vulnerability confirmation unless local evidence supports it.",
                "- Do not write case memory or research state from this packet.",
                "",
            ]
        )

        return "\n".join(lines)


def build_human_report_skeleton_packet(
    finding_draft_review_gate: dict[str, Any],
    source: str = "result-evidence-human-report-skeleton-packet",
) -> HumanReportSkeletonPacket:
    """Build a safe human report skeleton packet from a finding draft review gate."""
    _require_kind(
        finding_draft_review_gate,
        "result_evidence_finding_draft_packet_review_gate",
        "finding draft packet review gate",
    )

    title_findings = _object_list(
        finding_draft_review_gate.get("title_quality_findings"),
        "title_quality_findings",
    )
    evidence_findings = _object_list(
        finding_draft_review_gate.get("evidence_checklist_findings"),
        "evidence_checklist_findings",
    )
    reproduction_findings = _object_list(
        finding_draft_review_gate.get("reproduction_gap_findings"),
        "reproduction_gap_findings",
    )
    wording_findings = _object_list(
        finding_draft_review_gate.get("wording_guardrail_findings"),
        "wording_guardrail_findings",
    )
    blocked_claims = _object_list(
        finding_draft_review_gate.get("blocked_claim_findings"),
        "blocked_claim_findings",
    )
    do_not_claim = _object_list(
        finding_draft_review_gate.get("do_not_claim_findings"),
        "do_not_claim_findings",
    )
    safety_findings = _object_list(
        finding_draft_review_gate.get("safety_findings"),
        "safety_findings",
    )
    approved_support = _object_list(
        finding_draft_review_gate.get("approved_writing_support"),
        "approved_writing_support",
    )

    review_findings = title_findings + evidence_findings + reproduction_findings
    blockers = (
        _blocking_review_findings(review_findings)
        + blocked_claims
        + do_not_claim
        + safety_findings
    )
    blocking_status = "blocked-until-review-complete" if blockers else "human-write-required"

    support_subjects = _subjects(approved_support)
    title_subjects = _subjects(title_findings)
    evidence_subjects = _subjects(evidence_findings)
    reproduction_subjects = _subjects(reproduction_findings)
    wording_subjects = _subjects(wording_findings)
    blocked_subjects = _subjects(blocked_claims + do_not_claim + safety_findings)

    summary = HumanReportSkeletonSection(
        name="Summary",
        status=blocking_status,
        purpose="Short human-written explanation of the verified issue.",
        placeholder="[Human writes a concise summary from verified local evidence only.]",
        guidance=tuple(
            _dedupe(
                [
                    "Do not claim a confirmed vulnerability unless local proof exists.",
                    "Use approved writing support only as context.",
                    "Keep summary neutral while blockers remain open.",
                ]
            )
        ),
        source_items=tuple(support_subjects + title_subjects),
    )

    impact = HumanReportSkeletonSection(
        name="Impact",
        status="blocked-until-impact-evidence" if blockers or wording_findings else "human-write-required",
        purpose="Human-written impact statement tied to evidence.",
        placeholder="[Human writes impact only after proving exploitability and real security consequence.]",
        guidance=tuple(
            _dedupe(
                [
                    "Do not claim practical impact without local evidence.",
                    "Do not overstate business/security impact.",
                    "Resolve wording guardrails before impact writing.",
                ]
                + wording_subjects
            )
        ),
        source_items=tuple(wording_subjects + blocked_subjects),
    )

    steps = HumanReportSkeletonSection(
        name="Steps to Reproduce",
        status="blocked-until-reproduction-verified" if reproduction_findings or blockers else "human-write-required",
        purpose="Manual reproduction steps from verified actions only.",
        placeholder="[Human writes exact steps, requests, responses, screenshots, and expected/actual behavior.]",
        guidance=tuple(
            _dedupe(
                [
                    "Replace placeholders with verified manual steps.",
                    "Do not include commands or requests that were not actually validated.",
                    "Keep steps within authorized scope.",
                ]
                + reproduction_subjects
            )
        ),
        source_items=tuple(reproduction_subjects),
    )

    evidence = HumanReportSkeletonSection(
        name="Evidence",
        status="blocked-until-evidence-complete" if evidence_findings or blockers else "human-write-required",
        purpose="List of local evidence artifacts supporting the report.",
        placeholder="[Human lists evidence files, screenshots, request/response logs, and validation notes.]",
        guidance=tuple(
            _dedupe(
                [
                    "Every claim must map to evidence.",
                    "Do not rely on provider text as proof.",
                    "Close evidence checklist findings before report writing.",
                ]
                + evidence_subjects
            )
        ),
        source_items=tuple(evidence_subjects),
    )

    affected_assets = HumanReportSkeletonSection(
        name="Affected Assets",
        status="human-write-required",
        purpose="Assets, endpoints, packages, contracts, or components affected by the verified issue.",
        placeholder="[Human writes affected assets from verified scope and evidence only.]",
        guidance=(
            "Only include in-scope assets.",
            "Do not infer affected systems without evidence.",
            "Use exact identifiers from validated artifacts.",
        ),
        source_items=tuple(support_subjects),
    )

    severity = HumanReportSkeletonSection(
        name="Severity Rationale",
        status="blocked-until-severity-evidence" if blockers or wording_findings else "human-write-required",
        purpose="Human-written severity reasoning from exploitability and impact evidence.",
        placeholder="[Human writes severity only after evidence supports impact and exploitability.]",
        guidance=tuple(
            _dedupe(
                [
                    "Do not state High/Critical unless evidence supports it.",
                    "Keep severity conservative until blockers are resolved.",
                    "Separate likelihood from impact.",
                ]
                + wording_subjects
            )
        ),
        source_items=tuple(wording_subjects + blocked_subjects),
    )

    remediation = HumanReportSkeletonSection(
        name="Remediation",
        status="human-write-required",
        purpose="Suggested mitigation written by a human after root cause is clear.",
        placeholder="[Human writes remediation after confirming root cause and affected behavior.]",
        guidance=(
            "Avoid prescribing fixes without root-cause evidence.",
            "Prefer authorization, validation, least-privilege, and safe-default guidance where applicable.",
            "Keep remediation actionable and tied to the issue.",
        ),
        source_items=tuple(support_subjects),
    )

    blocked = HumanReportSkeletonSection(
        name="Blocked Claims / Do Not Claim",
        status="open-blockers" if blocked_subjects else "none-open",
        purpose="Claims that must not appear in a report yet.",
        placeholder="[Human removes or resolves all blocked/do-not-claim items before final report writing.]",
        guidance=tuple(
            _dedupe(
                [
                    "Do not include blocked claims in report text.",
                    "Do not claim vulnerability confirmation from skeleton metadata.",
                    "Re-run review after resolving blockers.",
                ]
                + blocked_subjects
            )
        ),
        source_items=tuple(blocked_subjects),
    )

    checklist = _final_checklist(
        finding_draft_review_gate,
        blockers=blockers,
        approved_support=approved_support,
        section_statuses=[
            summary.status,
            impact.status,
            steps.status,
            evidence.status,
            affected_assets.status,
            severity.status,
            remediation.status,
            blocked.status,
        ],
    )

    recommendation = _packet_recommendation(blockers, approved_support)

    return HumanReportSkeletonPacket(
        recommendation=recommendation,
        summary=summary,
        impact=impact,
        steps_to_reproduce=steps,
        evidence=evidence,
        affected_assets=affected_assets,
        severity_rationale=severity,
        remediation=remediation,
        blocked_claims_do_not_claim=blocked,
        human_final_writing_checklist=tuple(checklist),
        source=source,
    )


def _blocking_review_findings(items: list[dict[str, Any]]) -> list[dict[str, Any]]:
    blockers: list[dict[str, Any]] = []

    for item in items:
        severity = _optional_text(item.get("severity"), "info").lower()
        status = _optional_text(item.get("status"), "").lower()
        category = _optional_text(item.get("category"), "").lower()

        if severity in {"medium", "high", "critical"}:
            blockers.append(item)
            continue

        if "blocked" in status or "blocked" in category:
            blockers.append(item)
            continue

        if "missing" in status or "missing" in category:
            blockers.append(item)
            continue

        if "required-before" in status or "fix-required" in status:
            blockers.append(item)

    return blockers


def _packet_recommendation(
    blockers: list[dict[str, Any]],
    approved_support: list[dict[str, Any]],
) -> str:
    safety_blockers = [
        item
        for item in blockers
        if "safety" in _optional_text(item.get("category"), "").lower()
        or "safety" in _optional_text(item.get("source"), "").lower()
    ]
    if safety_blockers:
        return "do-not-build-report-fix-safety-metadata"

    if blockers:
        return "skeleton-built-but-report-writing-blocked"

    if approved_support:
        return "ready-as-human-report-skeleton-only"

    return "skeleton-built-no-approved-writing-support"


def _final_checklist(
    review_gate: dict[str, Any],
    blockers: list[dict[str, Any]],
    approved_support: list[dict[str, Any]],
    section_statuses: list[str],
) -> list[str]:
    checklist = [
        "Confirm this packet is a skeleton only, not a final report.",
        "Confirm no report was generated or submitted by Blackhole.",
        "Confirm no vulnerability is marked confirmed from this skeleton.",
        "Confirm every final report claim maps to local evidence.",
        "Confirm all affected assets are in scope.",
    ]

    checklist.extend(_string_list(review_gate.get("final_review_checklist")))

    if blockers:
        checklist.append("Resolve all blocked, do-not-claim, evidence, reproduction, and safety findings before final report writing.")

    if approved_support:
        checklist.append("Use approved writing support only as human-reviewed context.")

    if any("blocked" in status for status in section_statuses):
        checklist.append("Do not treat blocked skeleton sections as report-ready.")

    checklist.append("Have a human write and review the final report wording.")

    return _dedupe(checklist)


def _subjects(items: list[dict[str, Any]]) -> list[str]:
    output: list[str] = []
    for item in items:
        subject = _optional_text(item.get("subject"), "")
        if not subject:
            subject = _optional_text(item.get("text"), "")
        if subject:
            output.append(subject)
    return _dedupe(output)


def _render_section(section: HumanReportSkeletonSection) -> list[str]:
    lines = [
        f"## {section.name}",
        "",
        f"- Status: {section.status}",
        f"- Purpose: {section.purpose}",
        f"- Placeholder: {section.placeholder}",
        "",
        "Guidance:",
    ]

    for item in section.guidance:
        lines.append(f"- {item}")

    lines.extend(["", "Source items:"])
    if section.source_items:
        for item in section.source_items:
            lines.append(f"- {item}")
    else:
        lines.append("- none")

    lines.append("")
    return lines


def _require_kind(data: dict[str, Any], expected: str, label: str) -> None:
    if not isinstance(data, dict):
        raise ValueError(f"{label} must be an object")

    if data.get("kind") != expected:
        raise ValueError(f"{label} requires kind={expected}")


def _object_list(value: Any, label: str) -> list[dict[str, Any]]:
    if not isinstance(value, list):
        raise ValueError(f"human report skeleton packet requires {label} list")

    output: list[dict[str, Any]] = []
    for item in value:
        if not isinstance(item, dict):
            raise ValueError(f"each {label} item must be an object")
        output.append(item)

    return output


def _optional_text(value: Any, default: str) -> str:
    text = str(value or "").strip()
    return text or default


def _string_list(value: Any) -> list[str]:
    if not isinstance(value, list):
        return []
    return [str(item).strip() for item in value if str(item).strip()]


def _dedupe(items: list[str]) -> list[str]:
    seen: set[str] = set()
    output: list[str] = []

    for item in items:
        normalized = item.strip()
        if not normalized:
            continue

        key = normalized.lower()
        if key in seen:
            continue

        seen.add(key)
        output.append(normalized)

    return output
