"""
Review gate for human report skeleton packets.

This module reviews a v0.69 human report skeleton packet before a human turns
it into a report. It checks section completeness, blocker leakage, evidence
mapping gaps, unsupported impact/severity placeholders, blocked/do-not-claim
items, and whether the skeleton is safe only as a human-writing scaffold.

It does not generate final reports, submit reports, write case memory, write
research state, call providers, execute tools, launch browsers, send network
requests, mutate targets, or confirm vulnerabilities.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any


@dataclass(frozen=True)
class HumanReportSkeletonReviewFinding:
    subject: str
    category: str
    severity: str
    status: str
    source: str
    reason: str
    required_action: str

    def to_dict(self) -> dict[str, Any]:
        return {
            "subject": self.subject,
            "category": self.category,
            "severity": self.severity,
            "status": self.status,
            "source": self.source,
            "reason": self.reason,
            "required_action": self.required_action,
        }


@dataclass(frozen=True)
class HumanReportSkeletonReviewGate:
    recommendation: str
    section_completeness_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    blocker_leakage_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    evidence_mapping_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    impact_severity_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    blocked_do_not_claim_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    safety_findings: tuple[HumanReportSkeletonReviewFinding, ...]
    approved_skeleton_sections: tuple[HumanReportSkeletonReviewFinding, ...]
    final_review_checklist: tuple[str, ...]
    source: str = "result-evidence-human-report-skeleton-review-gate"
    planning_only: bool = True
    gate_state: str = "reviewed_not_report_generated"

    def to_dict(self) -> dict[str, Any]:
        return {
            "kind": "result_evidence_human_report_skeleton_review_gate",
            "source": self.source,
            "recommendation": self.recommendation,
            "section_completeness_findings": [item.to_dict() for item in self.section_completeness_findings],
            "blocker_leakage_findings": [item.to_dict() for item in self.blocker_leakage_findings],
            "evidence_mapping_findings": [item.to_dict() for item in self.evidence_mapping_findings],
            "impact_severity_findings": [item.to_dict() for item in self.impact_severity_findings],
            "blocked_do_not_claim_findings": [item.to_dict() for item in self.blocked_do_not_claim_findings],
            "safety_findings": [item.to_dict() for item in self.safety_findings],
            "approved_skeleton_sections": [item.to_dict() for item in self.approved_skeleton_sections],
            "final_review_checklist": list(self.final_review_checklist),
            "planning_only": self.planning_only,
            "gate_state": self.gate_state,
            "safety": {
                "local_only": True,
                "planning_only": True,
                "human_approval_required": True,
                "report_generation": False,
                "report_submission": False,
                "final_report": False,
                "state_mutation": False,
                "case_memory_write": False,
                "research_state_write": False,
                "network_interaction": False,
                "target_mutation": False,
                "tool_execution": False,
                "browser_execution": False,
                "llm_provider_calls": False,
                "provider_execution": False,
                "vulnerability_confirmation": False,
            },
        }

    def to_markdown(self, title: str = "Human Report Skeleton Review Gate") -> str:
        lines: list[str] = [
            f"# {title}",
            "",
            "## Status",
            "",
            f"- Recommendation: {self.recommendation}",
            f"- Gate state: {self.gate_state}",
            "- Final report generated by Blackhole: false",
            "- Report submission by Blackhole: false",
            "- Human approval required: true",
            "- Vulnerability confirmation: false",
            "- Planning-only: true",
            "",
            "## Section Completeness Findings",
            "",
        ]

        lines.extend(_render_findings(self.section_completeness_findings))
        lines.extend(["", "## Blocker Leakage Findings", ""])
        lines.extend(_render_findings(self.blocker_leakage_findings))
        lines.extend(["", "## Evidence Mapping Findings", ""])
        lines.extend(_render_findings(self.evidence_mapping_findings))
        lines.extend(["", "## Impact / Severity Findings", ""])
        lines.extend(_render_findings(self.impact_severity_findings))
        lines.extend(["", "## Blocked / Do-Not-Claim Findings", ""])
        lines.extend(_render_findings(self.blocked_do_not_claim_findings))
        lines.extend(["", "## Safety Findings", ""])
        lines.extend(_render_findings(self.safety_findings))
        lines.extend(["", "## Approved Skeleton Sections", ""])
        lines.extend(_render_findings(self.approved_skeleton_sections))

        lines.extend(["", "## Final Review Checklist", ""])
        for item in self.final_review_checklist:
            lines.append(f"- [ ] {item}")

        lines.extend(
            [
                "",
                "## Safety",
                "",
                "- This command reviews a report skeleton packet only.",
                "- Do not treat this review as a final report.",
                "- Do not submit anything from this review.",
                "- Do not claim impact, severity, affected assets, or vulnerability confirmation unless local evidence supports it.",
                "- Do not write case memory or research state from this review gate.",
                "",
            ]
        )

        return "\n".join(lines)


def build_human_report_skeleton_review_gate(
    human_report_skeleton: dict[str, Any],
    source: str = "result-evidence-human-report-skeleton-review-gate",
) -> HumanReportSkeletonReviewGate:
    """Review a human report skeleton packet before report writing."""
    _require_kind(
        human_report_skeleton,
        "result_evidence_human_report_skeleton_packet",
        "human report skeleton packet",
    )

    sections = {
        "summary": _section(human_report_skeleton, "summary"),
        "impact": _section(human_report_skeleton, "impact"),
        "steps_to_reproduce": _section(human_report_skeleton, "steps_to_reproduce"),
        "evidence": _section(human_report_skeleton, "evidence"),
        "affected_assets": _section(human_report_skeleton, "affected_assets"),
        "severity_rationale": _section(human_report_skeleton, "severity_rationale"),
        "remediation": _section(human_report_skeleton, "remediation"),
        "blocked_claims_do_not_claim": _section(human_report_skeleton, "blocked_claims_do_not_claim"),
    }

    completeness = _section_completeness_findings(sections)
    blocker_leakage = _blocker_leakage_findings(sections)
    evidence_mapping = _evidence_mapping_findings(sections)
    impact_severity = _impact_severity_findings(sections)
    blocked_do_not_claim = _blocked_do_not_claim_findings(sections["blocked_claims_do_not_claim"])
    safety = _safety_findings(human_report_skeleton)

    approved = _approved_sections(
        sections,
        completeness + blocker_leakage + evidence_mapping + impact_severity + blocked_do_not_claim + safety,
    )

    checklist = _final_review_checklist(
        human_report_skeleton,
        completeness=completeness,
        blocker_leakage=blocker_leakage,
        evidence_mapping=evidence_mapping,
        impact_severity=impact_severity,
        blocked_do_not_claim=blocked_do_not_claim,
        safety=safety,
        approved=approved,
    )

    recommendation = _recommendation(
        completeness=completeness,
        blocker_leakage=blocker_leakage,
        evidence_mapping=evidence_mapping,
        impact_severity=impact_severity,
        blocked_do_not_claim=blocked_do_not_claim,
        safety=safety,
        approved=approved,
    )

    return HumanReportSkeletonReviewGate(
        recommendation=recommendation,
        section_completeness_findings=tuple(_dedupe_findings(completeness)),
        blocker_leakage_findings=tuple(_dedupe_findings(blocker_leakage)),
        evidence_mapping_findings=tuple(_dedupe_findings(evidence_mapping)),
        impact_severity_findings=tuple(_dedupe_findings(impact_severity)),
        blocked_do_not_claim_findings=tuple(_dedupe_findings(blocked_do_not_claim)),
        safety_findings=tuple(_dedupe_findings(safety)),
        approved_skeleton_sections=tuple(_dedupe_findings(approved)),
        final_review_checklist=tuple(checklist),
        source=source,
    )


def _recommendation(
    completeness: list[HumanReportSkeletonReviewFinding],
    blocker_leakage: list[HumanReportSkeletonReviewFinding],
    evidence_mapping: list[HumanReportSkeletonReviewFinding],
    impact_severity: list[HumanReportSkeletonReviewFinding],
    blocked_do_not_claim: list[HumanReportSkeletonReviewFinding],
    safety: list[HumanReportSkeletonReviewFinding],
    approved: list[HumanReportSkeletonReviewFinding],
) -> str:
    if safety:
        return "do-not-use-skeleton-fix-safety-metadata"

    if blocker_leakage or blocked_do_not_claim:
        return "do-not-use-skeleton-resolve-blockers"

    high = [
        item
        for item in completeness + evidence_mapping + impact_severity
        if item.severity in {"high", "critical"}
    ]
    if high:
        return "skeleton-not-ready-fix-critical-section-findings"

    medium = [
        item
        for item in completeness + evidence_mapping + impact_severity
        if item.severity == "medium"
    ]
    if medium:
        return "skeleton-review-required-before-human-writing"

    if approved:
        return "safe-as-human-report-skeleton-only"

    return "no-report-skeleton-sections-ready"


def _section(data: dict[str, Any], key: str) -> dict[str, Any]:
    section = data.get(key)
    if not isinstance(section, dict):
        raise ValueError(f"human report skeleton packet requires {key} object")
    return section


def _section_completeness_findings(sections: dict[str, dict[str, Any]]) -> list[HumanReportSkeletonReviewFinding]:
    findings: list[HumanReportSkeletonReviewFinding] = []

    for key, section in sections.items():
        name = _optional_text(section.get("name"), key)
        status = _optional_text(section.get("status"), "")
        purpose = _optional_text(section.get("purpose"), "")
        placeholder = _optional_text(section.get("placeholder"), "")
        guidance = _string_list(section.get("guidance"))

        if not status:
            findings.append(_finding(name, "section-completeness", "medium", "missing-section-status", key, "Section is missing status.", "Regenerate with explicit status."))
        if not purpose:
            findings.append(_finding(name, "section-completeness", "medium", "missing-section-purpose", key, "Section is missing purpose.", "Regenerate with clear purpose."))
        if not placeholder:
            findings.append(_finding(name, "section-completeness", "high", "missing-section-placeholder", key, "Section is missing placeholder.", "Add a human-writing placeholder."))
        if not guidance:
            findings.append(_finding(name, "section-completeness", "medium", "missing-section-guidance", key, "Section is missing guidance.", "Add section guidance."))

    return findings


def _blocker_leakage_findings(sections: dict[str, dict[str, Any]]) -> list[HumanReportSkeletonReviewFinding]:
    findings: list[HumanReportSkeletonReviewFinding] = []

    for key, section in sections.items():
        name = _optional_text(section.get("name"), key)
        status = _optional_text(section.get("status"), "").lower()
        text = " ".join(
            [
                _optional_text(section.get("placeholder"), ""),
                " ".join(_string_list(section.get("guidance"))),
                " ".join(_string_list(section.get("source_items"))),
            ]
        ).lower()

        if key == "blocked_claims_do_not_claim" and status != "none-open":
            findings.append(_finding(name, "blocker-leakage", "high", "open-blockers-present", key, "Blocked claims remain open.", "Resolve blockers before report writing."))

        if key != "blocked_claims_do_not_claim" and _contains_blocker_terms(text):
            findings.append(_finding(name, "blocker-leakage", "high", "blocked-or-unsafe-content-leaked", key, "Blocked/unsafe wording appears in a report section.", "Remove blocked wording before use."))

    return _dedupe_findings(findings)


def _evidence_mapping_findings(sections: dict[str, dict[str, Any]]) -> list[HumanReportSkeletonReviewFinding]:
    evidence = sections["evidence"]
    status = _optional_text(evidence.get("status"), "").lower()
    source_items = _string_list(evidence.get("source_items"))
    findings: list[HumanReportSkeletonReviewFinding] = []

    if "blocked" in status:
        findings.append(_finding("Evidence", "evidence-mapping", "high", "evidence-section-blocked", "evidence", "Evidence section is blocked.", "Close evidence blockers."))

    if not source_items:
        findings.append(_finding("Evidence", "evidence-mapping", "info", "human-evidence-mapping-required", "evidence", "Evidence section has no mapped source items yet.", "Human must map final evidence before writing."))

    return findings


def _impact_severity_findings(sections: dict[str, dict[str, Any]]) -> list[HumanReportSkeletonReviewFinding]:
    findings: list[HumanReportSkeletonReviewFinding] = []

    for key in ("impact", "severity_rationale"):
        section = sections[key]
        name = _optional_text(section.get("name"), key)
        status = _optional_text(section.get("status"), "").lower()
        guidance = " ".join(_string_list(section.get("guidance"))).lower()
        placeholder = _optional_text(section.get("placeholder"), "").lower()

        if "blocked" in status:
            findings.append(_finding(name, "impact-severity", "high", "impact-or-severity-section-blocked", key, "Impact or severity section is blocked.", "Resolve blockers before writing this section."))

        if "do not" in guidance or "do not" in placeholder:
            findings.append(_finding(name, "impact-severity", "info", "wording-guardrail-present", key, "Protective wording guardrail is present.", "Apply guardrails during human writing."))

    return _dedupe_findings(findings)


def _blocked_do_not_claim_findings(section: dict[str, Any]) -> list[HumanReportSkeletonReviewFinding]:
    status = _optional_text(section.get("status"), "").lower()
    source_items = _string_list(section.get("source_items"))

    if status == "none-open" and not source_items:
        return []

    findings: list[HumanReportSkeletonReviewFinding] = []
    if status != "none-open":
        findings.append(_finding("Blocked Claims / Do Not Claim", "blocked-do-not-claim", "high", "blocked-claims-open", "blocked_claims_do_not_claim", "Blocked/do-not-claim items remain open.", "Resolve them before report writing."))

    for item in source_items:
        findings.append(_finding(item, "blocked-do-not-claim", "high", "do-not-claim-item-open", "blocked_claims_do_not_claim", "Item is explicitly blocked or do-not-claim.", "Keep it out of report text until revalidated."))

    return _dedupe_findings(findings)


def _safety_findings(packet: dict[str, Any]) -> list[HumanReportSkeletonReviewFinding]:
    safety = packet.get("safety")
    if not isinstance(safety, dict):
        return [_finding("safety", "safety-metadata", "high", "missing-safety-metadata", "safety", "Packet is missing safety metadata.", "Regenerate with explicit safety metadata.")]

    required_false = [
        "report_generation",
        "report_submission",
        "final_report",
        "state_mutation",
        "case_memory_write",
        "research_state_write",
        "network_interaction",
        "target_mutation",
        "tool_execution",
        "browser_execution",
        "llm_provider_calls",
        "provider_execution",
        "vulnerability_confirmation",
    ]

    findings: list[HumanReportSkeletonReviewFinding] = []
    for key in required_false:
        if safety.get(key) is not False:
            findings.append(_finding(key, "safety-metadata", "high", "unsafe-safety-metadata", "safety", f"Safety metadata must keep {key}=false.", "Regenerate with safe metadata."))

    if safety.get("human_approval_required") is not True:
        findings.append(_finding("human_approval_required", "safety-metadata", "medium", "missing-human-approval-required", "safety", "Human approval must be required.", "Regenerate with human_approval_required=true."))

    return findings


def _approved_sections(
    sections: dict[str, dict[str, Any]],
    all_findings: list[HumanReportSkeletonReviewFinding],
) -> list[HumanReportSkeletonReviewFinding]:
    blocking_sources = {
        finding.source
        for finding in all_findings
        if finding.severity in {"medium", "high", "critical"}
    }

    output: list[HumanReportSkeletonReviewFinding] = []
    for key, section in sections.items():
        if key in blocking_sources:
            continue

        name = _optional_text(section.get("name"), key)
        output.append(_finding(name, "approved-skeleton-section", "info", "skeleton-section-review-support-only", key, "Section is available as human-writing scaffold.", "Use only as scaffold, not a final report."))

    return output


def _final_review_checklist(
    packet: dict[str, Any],
    completeness: list[HumanReportSkeletonReviewFinding],
    blocker_leakage: list[HumanReportSkeletonReviewFinding],
    evidence_mapping: list[HumanReportSkeletonReviewFinding],
    impact_severity: list[HumanReportSkeletonReviewFinding],
    blocked_do_not_claim: list[HumanReportSkeletonReviewFinding],
    safety: list[HumanReportSkeletonReviewFinding],
    approved: list[HumanReportSkeletonReviewFinding],
) -> list[str]:
    checklist = [
        "Confirm this review gate is used only for human report writing support.",
        "Confirm no final report was generated or submitted by Blackhole.",
        "Confirm no vulnerability is marked confirmed from the skeleton.",
        "Confirm every final report claim maps to local evidence.",
    ]

    checklist.extend(_string_list(packet.get("human_final_writing_checklist")))

    if completeness:
        checklist.append("Resolve section completeness findings before report writing.")
    if blocker_leakage:
        checklist.append("Remove blocker leakage from report sections.")
    if evidence_mapping:
        checklist.append("Map evidence before writing the final report.")
    if impact_severity:
        checklist.append("Apply impact and severity guardrails during human writing.")
    if blocked_do_not_claim:
        checklist.append("Keep blocked/do-not-claim items out of report text.")
    if safety:
        checklist.append("Fix safety metadata before using the skeleton.")
    if approved:
        checklist.append("Use approved skeleton sections only as scaffold text.")

    return _dedupe(checklist)


def _finding(
    subject: str,
    category: str,
    severity: str,
    status: str,
    source: str,
    reason: str,
    required_action: str,
) -> HumanReportSkeletonReviewFinding:
    return HumanReportSkeletonReviewFinding(
        subject=subject,
        category=category,
        severity=severity,
        status=status,
        source=source,
        reason=reason,
        required_action=required_action,
    )


def _render_findings(findings: tuple[HumanReportSkeletonReviewFinding, ...]) -> list[str]:
    if not findings:
        return ["- none"]

    lines: list[str] = []
    for finding in findings:
        lines.append(f"- **{finding.status} / {finding.severity}**: {finding.subject}")
        lines.append(f"  - Category: {finding.category}")
        lines.append(f"  - Source: {finding.source}")
        lines.append(f"  - Reason: {finding.reason}")
        lines.append(f"  - Required action: {finding.required_action}")

    return lines


def _require_kind(data: dict[str, Any], expected: str, label: str) -> None:
    if not isinstance(data, dict):
        raise ValueError(f"{label} must be an object")

    if data.get("kind") != expected:
        raise ValueError(f"{label} requires kind={expected}")


def _optional_text(value: Any, default: str) -> str:
    text = str(value or "").strip()
    return text or default


def _string_list(value: Any) -> list[str]:
    if not isinstance(value, list):
        return []
    return [str(item).strip() for item in value if str(item).strip()]


def _contains_blocker_terms(value: str) -> bool:
    terms = (
        "unsafe",
        "blocked",
        "do not claim",
        "not claim",
        "open blocker",
        "missing evidence",
        "without evidence",
    )
    return any(term in value for term in terms)


def _dedupe(items: list[str]) -> list[str]:
    seen: set[str] = set()
    output: list[str] = []

    for item in items:
        normalized = item.strip()
        if not normalized:
            continue

        key = normalized.lower()
        if key in seen:
            continue

        seen.add(key)
        output.append(normalized)

    return output


def _dedupe_findings(findings: list[HumanReportSkeletonReviewFinding]) -> list[HumanReportSkeletonReviewFinding]:
    seen: set[tuple[str, str, str]] = set()
    output: list[HumanReportSkeletonReviewFinding] = []

    for finding in findings:
        key = (finding.status.lower(), finding.category.lower(), finding.subject.lower())
        if key in seen:
            continue

        seen.add(key)
        output.append(finding)

    return output
